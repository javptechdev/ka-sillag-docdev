export const getGreeting = (): string => {
  const hour = new Date().getHours();
  if (hour >= 4 && hour < 12) return 'Magandang Umaga Ka-Sillag!';
  if (hour >= 12 && hour < 18) return 'Magandang Hapon Ka-Sillag!';
  return 'Magandang Gabi Ka-Sillag!';
};
